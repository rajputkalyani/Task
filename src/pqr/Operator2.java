package pqr;

public class Operator2 {
	
	// Relational Operators
	
	public void main()
	{
		int a=55;
		int b=34;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a==b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a!=b);
	}

	public static void main(String[] args) {
		
		Operator2 v = new Operator2();
		v.main();

	}

}
